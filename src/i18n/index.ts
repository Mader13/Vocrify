export type I18nMessages = typeof messages;
export type I18nLocale = keyof I18nMessages;

export const DEFAULT_LOCALE: I18nLocale = "en";
export const SUPPORTED_LOCALES = Object.keys(messages) as I18nLocale[];

type Join<K, P> = K extends string ? (P extends string ? `${K}.${P}` : never) : never;

type NestedKeys<T> = T extends object
  ? {
      [K in keyof T]: K extends string ? (T[K] extends object ? K | Join<K, NestedKeys<T[K]>> : K) : never;
    }[keyof T]
  : never;

export type I18nKey = NestedKeys<I18nMessages["en"]>;

export type TranslateFn = (key: I18nKey) => string;

function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null;
}

function getValueByPath(source: unknown, key: string): string | undefined {
  const parts = key.split(".");
  let current: unknown = source;

  for (const part of parts) {
    if (!isRecord(current)) {
      return undefined;
    }

    current = current[part];
  }

  return typeof current === "string" ? current : undefined;
}

export function normalizeLocale(locale: string | null | undefined): I18nLocale {
  if (locale && SUPPORTED_LOCALES.includes(locale as I18nLocale)) {
    return locale as I18nLocale;
  }

  return DEFAULT_LOCALE;
}

export function translate(locale: string | null | undefined, key: I18nKey): string {
  const normalizedLocale = normalizeLocale(locale);
  const localeValue = getValueByPath(messages[normalizedLocale], key);

  if (localeValue) {
    return localeValue;
  }

  const fallbackValue = getValueByPath(messages[DEFAULT_LOCALE], key);
  if (fallbackValue) {
    return fallbackValue;
  }

  return key;
}
